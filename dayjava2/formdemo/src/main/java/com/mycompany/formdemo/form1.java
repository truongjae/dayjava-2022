    public void loadData(){
        model.setRowCount(0);
        for(Student s: arr){
            Vector rowData = new Vector();
            rowData.add(s.getId());
            rowData.add(s.getName());
            rowData.add(s.getAge());
            rowData.add(s.getSex());
            rowData.add(s.getAddress());
            model.addRow(rowData);
        }
    }

