    private void btnTenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTenActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText();
        String name = txtName.getText();
        String sex = txtSex.getText();
        String address = txtAddress.getText();
        boolean check = true;
        try{
             int age = Integer.parseInt(txtAge.getText());
             Student st = new Student(id,name,age,sex,address);
            arr.add(st);
            loadData();
        }
        catch(NumberFormatException e){
            check = false;
            JOptionPane.showMessageDialog(this, "tuoi phai la so");
        }
        
    }//GEN-LAST:event_btnTenActionPerformed

